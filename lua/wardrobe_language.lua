wardrobe.language = {}
wardrobe.language.current = wardrobe.config.defaultLanguage or "english"

local LANGUAGE_FILE = "wardrobe_language.txt"

wardrobe.language.available = {
	english   = "gb",
	russian   = "ru",
	french    = "fr",
	dutch     = "nl",
	german    = "de",
	spanish   = "es",
	norwegian = "no",
	finnish   = "fi",
	swedish   = "se",
	ukrainian = "ua",
}

if file.Exists(LANGUAGE_FILE, "DATA") then
	local s = file.Read(LANGUAGE_FILE, "DATA")

	if s and #s > 0 then
		if wardrobe.language.available[s] then
			wardrobe.language.current = s
		else
			print("Wardrobe | Language selection saved but doesn't exist?")
		end
	end
elseif not wardrobe.language.available[wardrobe.language.current] then
	wardrobe.language.current = "english"
end

function wardrobe.language.icon()
	return wardrobe.language.available[wardrobe.language.current or "english"]
end

